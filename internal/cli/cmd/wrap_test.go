func TestNewWrapCmd_RenderedYAMLValid(t *testing.T) {
	// This test verifies that rendered vibewarden.yaml is non-empty and contains
	// expected keys.
	tests := []struct {
		name       string
		args       []string
		wantInYAML []string
	}{
		{
			name:       "default config contains server and upstream sections",
			args:       []string{},
			wantInYAML: []string{"server:", "upstream:", "port: 3000", "port: 8443"},
		},
		{
			name:       "default config enables TLS with self-signed provider",
			args:       []string{},
			wantInYAML: []string{"tls:", "enabled: true", "self-signed"},
		},
		{
			name:       "auth flag adds kratos and auth sections with new consolidated fields",
			args:       []string{"--auth"},
			wantInYAML: []string{"kratos:", "public_url:", "dsn:", "smtp:", "auth:", "identity_schema:"},
		},
		{
			name:       "rate-limit flag adds rate_limit section",
			args:       []string{"--rate-limit"},
			wantInYAML: []string{"rate_limit:", "per_ip:"},
		},
		{
			name:       "tls flag adds tls section with domain",
			args:       []string{"--tls", "--domain", "example.com"},
			wantInYAML: []string{"tls:", "enabled: true", "example.com"},
		},
		{
			name:       "upstream port appears in config",
			args:       []string{"--upstream", "4200"},
			wantInYAML: []string{"port: 4200"},
		},
		{
			name:       "overrides section is present as comment",
			args:       []string{},
			wantInYAML: []string{"overrides"},
		},
		{
			name:       "app.build defaults to current directory",
			args:       []string{},
			wantInYAML: []string{`build: "."`},
		},
	}

	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			dir := t.TempDir()

			root := cmd.NewRootCmd("test")
			allArgs := append([]string{"wrap", dir}, tt.args...)
			root.SetArgs(allArgs)

			if err := root.Execute(); err != nil {
				t.Fatalf("Execute() unexpected error: %v", err)
			}

			yamlBytes, err := os.ReadFile(filepath.Join(dir, "vibewarden.yaml"))
			if err != nil {
				t.Fatalf("reading vibewarden.yaml: %v", err)
			}
			yamlContent := string(yamlBytes)

			for _, want := range tt.wantInYAML {
				if !strings.Contains(yamlContent, want) {
					t.Errorf("vibewarden.yaml does not contain %q\n\nContent:\n%s", want, yamlContent)
				}
			}
		})
	}
}

// TestNewWrapCmd_AppBuildDefaultsToDot verifies that the generated
// vibewarden.yaml defaults to app.build = "." rather than app.image.
func TestNewWrapCmd_AppBuildDefaultsToDot(t *testing.T) {
	parent := t.TempDir()
	projectDir := filepath.Join(parent, "mywebapp")
	if err := os.MkdirAll(projectDir, 0o750); err != nil {
		t.Fatalf("mkdir: %v", err)
	}

	root := cmd.NewRootCmd("test")
	root.SetArgs([]string{"wrap", projectDir})

	if err := root.Execute(); err != nil {
		t.Fatalf("Execute() unexpected error: %v", err)
	}

	yamlBytes, err := os.ReadFile(filepath.Join(projectDir, "vibewarden.yaml"))
	if err != nil {
		t.Fatalf("reading vibewarden.yaml: %v", err)
	}
	content := string(yamlBytes)

	if !strings.Contains(content, `build: "."`) {
		t.Errorf("vibewarden.yaml does not contain active 'build: \".\"'\n\nContent:\n%s", content)
	}
	// "image:" must not appear as an active (uncommented) directive.
	for _, line := range strings.Split(content, "\n") {
		trimmed := strings.TrimSpace(line)
		if strings.HasPrefix(trimmed, "image:") {
			t.Errorf("vibewarden.yaml must not have an active 'image:' directive by default; found: %q\n\nContent:\n%s", line, content)
		}
	}
}

func TestNewWrapCmd_SuccessMessage(t *testing.T) {
	t.Run("success message mentions vibew dev", func(t *testing.T) {
		dir := t.TempDir()
		root := cmd.NewRootCmd("test")
		var outBuf strings.Builder
		root.SetOut(&outBuf)
		root.SetArgs([]string{"wrap", dir})

		if err := root.Execute(); err != nil {
			t.Fatalf("Execute() unexpected error: %v", err)
		}

		out := outBuf.String()
		if !strings.Contains(out, "vibew dev") {
			t.Errorf("success message does not mention 'vibew dev'\n\nOutput:\n%s", out)
		}
		if !strings.Contains(out, "VibeWarden added to project!") {
			t.Errorf("success message does not contain 'VibeWarden added to project!'\n\nOutput:\n%s", out)
		}
	})

	t.Run("success message does not mention docker compose up", func(t *testing.T) {
		dir := t.TempDir()
		root := cmd.NewRootCmd("test")
		var outBuf strings.Builder
		root.SetOut(&outBuf)
		root.SetArgs([]string{"wrap", dir})

		if err := root.Execute(); err != nil {
			t.Fatalf("Execute() unexpected error: %v", err)
		}

		out := outBuf.String()
		if strings.Contains(out, "docker compose up") {
			t.Errorf("success message must not mention 'docker compose up'\n\nOutput:\n%s", out)
		}
	})
}
