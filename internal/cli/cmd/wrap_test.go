func TestNewWrapCmd_FlagCombinations(t *testing.T) {
	tests := []struct {
		name            string
		args            []string
		wantErr         bool
		wantOutContains string
		setup           func(dir string)
		checkFiles      []string
		absentFiles     []string
	}{
		{
			name:            "defaults generate vibewarden.yaml without docker-compose",
			args:            []string{},
			checkFiles:      []string{"vibewarden.yaml"},
			absentFiles:     []string{"docker-compose.yml"},
			wantOutContains: "VibeWarden added to project!",
		},
		{
			name:        "docker-compose.yml is never generated by wrap",
			args:        []string{},
			absentFiles: []string{"docker-compose.yml"},
		},
		{
			name:    "tls without domain returns error",
			args:    []string{"--tls"},
			wantErr: true,
		},
		{
			name:        "tls with domain succeeds without docker-compose",
			args:        []string{"--tls", "--domain", "example.com"},
			checkFiles:  []string{"vibewarden.yaml"},
			absentFiles: []string{"docker-compose.yml"},
		},
		{
			name:        "auth flag enabled produces vibewarden.yaml only",
			args:        []string{"--auth"},
			checkFiles:  []string{"vibewarden.yaml"},
			absentFiles: []string{"docker-compose.yml"},
		},
		{
			name:        "rate-limit flag enabled produces vibewarden.yaml only",
			args:        []string{"--rate-limit"},
			checkFiles:  []string{"vibewarden.yaml"},
			absentFiles: []string{"docker-compose.yml"},
		},
		{
			name:        "upstream port set",
			args:        []string{"--upstream", "8080"},
			checkFiles:  []string{"vibewarden.yaml"},
			absentFiles: []string{"docker-compose.yml"},
		},
		{
			name: "force flag overwrites existing vibewarden.yaml",
			args: []string{"--force"},
			setup: func(dir string) {
				if err := os.WriteFile(filepath.Join(dir, "vibewarden.yaml"), []byte("old"), 0o644); err != nil {
					t.Fatal(err)
				}
			},
			checkFiles: []string{"vibewarden.yaml"},
		},
		{
			name: "existing vibewarden.yaml without force returns error",
			args: []string{},
			setup: func(dir string) {
				if err := os.WriteFile(filepath.Join(dir, "vibewarden.yaml"), []byte("old"), 0o644); err != nil {
					t.Fatal(err)
				}
			},
			wantErr: true,
		},
		{
			name: "existing docker-compose.yml is left untouched (no error)",
			args: []string{},
			setup: func(dir string) {
				if err := os.WriteFile(filepath.Join(dir, "docker-compose.yml"), []byte("old"), 0o644); err != nil {
					t.Fatal(err)
				}
			},
			// docker-compose.yml exists before wrap and is not touched — no error expected.
			checkFiles: []string{"vibewarden.yaml", "docker-compose.yml"},
		},
	}

	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			dir := t.TempDir()

			if tt.setup != nil {
				tt.setup(dir)
			}

			// Build the cobra root and add the wrap command.
			root := cmd.NewRootCmd("test")
			var outBuf bytes.Buffer
			root.SetOut(&outBuf)

			// Run: vibewarden wrap <dir> <flags...>
			allArgs := append([]string{"wrap", dir}, tt.args...)
			root.SetArgs(allArgs)

			err := root.Execute()

			if (err != nil) != tt.wantErr {
				t.Fatalf("Execute() error = %v, wantErr %v", err, tt.wantErr)
			}

			if tt.wantOutContains != "" {
				if !strings.Contains(outBuf.String(), tt.wantOutContains) {
					t.Errorf("output %q does not contain %q", outBuf.String(), tt.wantOutContains)
				}
			}

			for _, filename := range tt.checkFiles {
				path := filepath.Join(dir, filename)
				if _, err := os.Stat(path); err != nil {
					t.Errorf("expected file %q to exist: %v", path, err)
				}
			}

			for _, filename := range tt.absentFiles {
				path := filepath.Join(dir, filename)
				if _, err := os.Stat(path); err == nil {
					t.Errorf("file %q should not exist but does", path)
				}
			}
		})
	}
}

